////////////////////////////////////////////////////////////////////////////////
//                     GROUP ALL TRAINERS ROUTES                              //
////////////////////////////////////////////////////////////////////////////////
var trainerRoutes = FlowRouter.group({
  name: 'trainer',
});

////////////////////////////////////////////////////////////////////////////////
//                     ADD NEW CLIENT ROUTE                                   //
////////////////////////////////////////////////////////////////////////////////
trainerRoutes.route('/addClient', {
  action: function() {
    BlazeLayout.render('layout', { main: "addClient" });
  },
});

////////////////////////////////////////////////////////////////////////////////
//                    CLIENT DASHBOARD ROUTE                                  //
////////////////////////////////////////////////////////////////////////////////
trainerRoutes.route('/clientDashboard/:_id', {
  action: function() {
    BlazeLayout.render('layout', { main: "clientDashboard" });
  },
});

////////////////////////////////////////////////////////////////////////////////
//                    MY CURRENT CLIENTS ROUTE                                //
////////////////////////////////////////////////////////////////////////////////
trainerRoutes.route('/currentClients', {
  action: function() {
    BlazeLayout.render('layout', { main: "currentClients" });
  },
});

////////////////////////////////////////////////////////////////////////////////
//                    Calendar Route                                          //
////////////////////////////////////////////////////////////////////////////////
trainerRoutes.route('/calendar', {
  action: function() {
    BlazeLayout.render('layout', { main: "calendar" });
  },
});
