////////////////////////////////////////////////////////////////////////////////
//                  GROUP SHARED USER ROUTES                                  //
////////////////////////////////////////////////////////////////////////////////
var allUsersRoutes = FlowRouter.group({
  name: 'allUsers',
});

////////////////////////////////////////////////////////////////////////////////
//                   HOME PAGE ROUTE                                          //
////////////////////////////////////////////////////////////////////////////////
allUsersRoutes.route('/', {
  action: function() {
    BlazeLayout.render('layout', { main: "home" });
  }
});

////////////////////////////////////////////////////////////////////////////////
//                    LOGIN PAGE ROUTE                                        //
////////////////////////////////////////////////////////////////////////////////
allUsersRoutes.route('/login', {
  action: function() {
    BlazeLayout.render('layout', { main: "login" });
  }
});

////////////////////////////////////////////////////////////////////////////////
//                   REGISTER TRAINER ROUTE                                   //
////////////////////////////////////////////////////////////////////////////////
allUsersRoutes.route('/register', {
  action: function() {
    BlazeLayout.render('layout', { main: "register" });
  }
});

////////////////////////////////////////////////////////////////////////////////
//                    My Profile Route                                        //
////////////////////////////////////////////////////////////////////////////////
allUsersRoutes.route('/myProfile', {
  action: function() {
    BlazeLayout.render('layout', { main: "myProfile" });
  }
});
