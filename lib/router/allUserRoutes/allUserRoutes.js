////////////////////////////////////////////////////////////////////////////////
//                  GROUP SHARED USER ROUTES                                  //
////////////////////////////////////////////////////////////////////////////////
var allUsersRoutes = FlowRouter.group({
  name: 'allUsers',
});

////////////////////////////////////////////////////////////////////////////////
//                   HOME PAGE ROUTE                                          //
////////////////////////////////////////////////////////////////////////////////
